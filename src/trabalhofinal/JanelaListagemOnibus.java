    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int pesquisa2;
        pesquisa2 = Integer.parseInt(txtPesquisa2.getText());
        
        LeituraParadasLinhas paradasLinhas = new LeituraParadasLinhas();
        LeituraLinhasDeOnibus linha = new LeituraLinhasDeOnibus();
        try {
            paradasLinhas.leituraParadasLinhas();
            linha.leituraLinhasDeOnibus();
            
            // Guarda retorno de linhas dos onibus
            ArrayList<ParadaLinha> linhasOnibus;
            linhasOnibus = paradasLinhas.pegaLinhasOnibus(pesquisa2);
            
            // Guarda retorno de linhas
            ArrayList<LinhaDeOnibus> linhas;
            linhas = linha.pegaLinhasDaParada();
            for(int i = 0; i < linhasOnibus.size(); i++){
                for(int j = 0; j < linha.size(); j++){
                    if(linhasOnibus.get(i).getId() == linhas.get(j).getIdLinha()){
                        txtResultado.append(String.valueOf(linhas.get(j).getCodigo()) + ": Nome = " + String.valueOf(linhas.get(j).getNome())+"\n");
                    }
                }
               
            }
                
        } catch (FileNotFoundException ex) {
            Logger.getLogger(JanelaListagemOnibus.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            //Logger.getLogger(JanelaListagemOnibus.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

