    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        int pesquisa;
        pesquisa = Integer.parseInt(txtPesquisa.getText());;
        
        LeituraParadasLinhas paradasLinhas = new LeituraParadasLinhas();
        LeituraParadas parada = new LeituraParadas();
        try {
            paradasLinhas.leituraParadasLinhas();
            parada.leituraParadas();
            
            // Guarda retorno de paradas dos onibus
            ArrayList<ParadaLinha> paradasOnibus;
            paradasOnibus = paradasLinhas.pegaParadasOnibus(pesquisa);
            
            // Guarda retorno de paradas
            ArrayList<Parada> paradas;
            paradas = parada.pegaParadas();
            for(int i = 0; i < paradasOnibus.size(); i++){
                for(int j = 0; j < parada.size(); j++){
                    if(paradasOnibus.get(i).getIdParada() == paradas.get(j).getIdParada()){
                        txtResultado.append(String.valueOf(paradas.get(j).getCodigo()) + ": Lat = " + String.valueOf(paradas.get(j).getLatitude()) + ": Lon = " + String.valueOf(paradas.get(j).getLongitude()) + "\n");
                    }
                }
               
            }
                
        } catch (FileNotFoundException ex) {
            Logger.getLogger(JanelaListagemOnibus.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(JanelaListagemOnibus.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

